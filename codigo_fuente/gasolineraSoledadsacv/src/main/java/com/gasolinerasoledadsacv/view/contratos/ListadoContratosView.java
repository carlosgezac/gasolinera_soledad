    protected void llenarTabla(String numEmpleado) {
        //Llenar tabla
        try {
            contratoJpaController = new ContratoJpaController(emf);
            empleadoJpaController = new EmpleadoJpaController(emf);
            Empleado empleado = empleadoJpaController.findByNumeroEmpleado(numEmpleado);
            labelEmpleado.setText(empleado.getNumeroEmpleado() + " - " + empleado.getNombre() + " " + empleado.getApellidoPaterno() + " " + empleado.getApellidoMaterno());
            List<Contrato> listaContrato = contratoJpaController.findByEmpleado(empleado);
            DefaultTableModel model = (DefaultTableModel) tablaContratos.getModel();
            model.setRowCount(0);
            AtomicInteger numContrato = new AtomicInteger(1);
            listaContrato.forEach(contrato -> {
                model.addRow(new Object[]{contrato.getIdContrato(), numContrato.getAndIncrement(), DateUtil.formatDefault(contrato.getFechaInicial()), DateUtil.formatDefault(contrato.getFechaFinal()), DateUtil.substract(contrato.getFechaInicial(), contrato.getFechaFinal()), contrato.getFechaFiniquito() != null ? DateUtil.formatDefault(contrato.getFechaFiniquito()) : ""});
            });
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "No se pudo cargar la información de los contratos.\nIntentelo más tarde.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

