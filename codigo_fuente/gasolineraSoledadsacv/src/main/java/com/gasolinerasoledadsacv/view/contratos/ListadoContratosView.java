    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        //El usuario debe seleccionar una fila, de lo contrario no se podra mostrar el formulario de eDITAR/gUARDAR
        if (tablaContratos.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int idContrato = Integer.parseInt(tablaContratos.getValueAt(tablaContratos.getSelectedRow(), 0).toString());
        NuevoContratoView nuevoContratoView = new NuevoContratoView(idContrato, this);
        nuevoContratoView.setVisible(true);
        nuevoContratoView.setTitle("Editar Contrato");
    }//GEN-LAST:event_btnEditarActionPerformed


